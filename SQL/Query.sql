CREATE TABLE [MANASBHARATI].[SLIDER] (
MAPID INT IDENTITY(1,1) NOT NULL,
TITLE NVARCHAR(500) NULL,
IMG NVARCHAR(250) NULL,
BTNNAME NVARCHAR(250) NULL,
URL NVARCHAR(MAX) NULL,
