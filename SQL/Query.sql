TOTALMARKS DECIMAL,
PERCENTAGE DECIMAL,
CREATEDATE DATETIME,
UPDATEDATE DATETIME
)

CREATE TABLE [MANASBHARATI].[NOTICE] (
MAPID INT IDENTITY(1,1),
TITLE NVARCHAR(3000),
URL NVARCHAR(MAX),
