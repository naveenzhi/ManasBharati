CREATE TABLE [MANASBHARATI].[RESULT] (
MAPID INT IDENTITY(1,1),
REGNO INT,
ROLLNO INT,
CLASS NVARCHAR(300),
CLASS_SECTION NVARCHAR(10),
SUBJECTNAME NVARCHAR(100),
PERIODIC DECIMAL,
